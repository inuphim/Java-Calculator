    private void btn_equalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_equalActionPerformed
       snum = Double.parseDouble(output.getText());
        if(ops=="+"){
           res = fnum+snum;
       }else if(ops=="/"){
           res = fnum/snum;
       }else if(ops=="*"){
           res= fnum*snum;
       }else if(ops=="-"){
           res = fnum-snum;
       }else if(ops=="%"){
           res = fnum%snum;
        }else{
           res = 0;
       }
        output.setText(res+"");
    }//GEN-LAST:event_btn_equalActionPerformed

